      <li>
        Nur vier Gruppen, die den aktiven Flug beherrschen: 1. Fluginsekten 2. Flugsaurier 3. Vögel 4. Fledertiere<o/><o/>
      </li>
      <li>
        Flügel: Neue Struktur, die vorher nicht da war (nicht aus Vorderextremitäten wie bei anderen Flugtieren).<o/><o/>
      </li>
      <li>
        Genregulatorisches Netzwerk, das Flügel bildet, findet sich an Beinstrukturen (Kiemen) von anderen Euarthropoda, aber dennoch Flügel wahrscheinlich nicht aus Kiemen entstanden, da diese verloren gegangen.<o/><o/>
      </li>
      <li>
        Genregulatorisches Netzwerk, das am Bein best. Strukturen hervorbringen kann, das jetzt co-optiert wird (an anderer Stelle ausgeprägt wird) dorsal an der Rückenplatte, wo das Tergit ist an einer Stelle, wo eigentlich kein Bein ist.<o/><o/>
      </li>
      <li>
        2 Theorien zur Evolution des Fliegens: bottom-up: Laufende Tiere, dann fliegen (Insekten auf Wasseroberfläche, präferiert vom Prof lol) top-down: Kletternde Tiere und dann fallen lassen. (Silberfischchen)<o/><o/>
      </li>
      <li>
        Ontogenese: Flügel zunächst mit Rückenplatte verbunden. Dann Verengung, die Flügel abschnürt + Gelenkformation (Kutikula krümmt sich ein). Flügel bekommen Korrugation (hin und her Knickung) zur Versteifung, um mechanisch zu stabilisieren.<o/><o/>
      </li>
      <li>
        Direkte (komplexeres Fliegen) vs. indirekte (einfacheres Fliegen) Flugmuskulatur. Indirekte Flugmuskulatur besteht aus Muskeln, die den ganzen vorderen Rumpfbereich verformen (DLM und DVM). Insekten brauchen hohe Frequenzen, also großes sarkoplasmatisches Retikulum. --&gt; Trigger-Effekte (Auslösungen, die den Muskeln mehrmals kontrahieren lassen) --&gt; Kleines Sarkoplasmatisches Retikulum trotzdem hohe Frequenz.<o/><o/>
      </li>
      <li>
        Finale Häutung zum Adult.<o/><o/>
      </li>
    </ul>
