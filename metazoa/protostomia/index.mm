      </li>
      <li>
        Beine bestehen aus Glieder. Glieder sind mit Scharniergelenken verbunden (nicht mit Gelenken bei Menschen zu vergleichen). Gelenk wird durch Cuticula gebildet durch Einfaltung in sich selbst. Zwei Gelenkknöpfe, die eine Bewegung in eine Achse ermöglich. Diese Gelenke ermöglichen große Kraft und schnelle Bewegungen.<o/><o/>
