        Abschnitt 1: proximal (körpernah) [Basipodit] kann als Kaulade ausgebildet sein und trägt median (zur Körpermitte hin) Stacheln oder Fortsätze mit Häärchen. Wird zum Kauen, Filtern und Schieben der Nahrung in den Mund benutzt.<o/><o/>
      </li>
      <li>
        Abschnitt 2: distal (körperfern) [Endopodit] normalerweise in mehrere Glieder unterteilt. Wird als &quot;Laufast&quot; bezeichnet. Da aber Gruppe zuerst Marin war, muss es nicht immer zum Laufen genutzt worden sein. Auch zum Greifen von Nahrungsmitteln benutzt mittels Gelenke.<o/><o/>
      </li>
      <li>
