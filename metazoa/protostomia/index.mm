      <li>
        Kopf umfasst mindestens fünf Segmente. Heute aber viel länger. 1. Segment: Augen, keine Beine. Späteren Segmente Multifunktionsbeine. 2. Segment: Ein Paar Beine. Urspürnglich für Greifen, Schwimmen und Sensorik. Später als Fühler. Mindestens zwei Sehsysteme und Komplexaugen.<o/><o/>
